# Type Casting:

'''
    Def: The process of converting a variable from one data type to another data type.
    str(), int(), float(),bool()

    # Casting in python is therefore done using constructor functions:

    int() - constructs an integer number from an integer literal, a float literal, or a string literal.
    float() - constructs a float number from an integer literal, a float literal or a string literal.
    str() - constructs a string from a wide variety of data types, including strings, integer literals and float literals.

'''

# Integers:
x = int(1)    # x will be 1
y = int(2.9)  # y will be 2
z = int("3")  # z will be 3
print(x)
print(y)
print(z)


# Floats:
a = float(1)   # a will be 1.0
b = float(2.9) # b will be 2.9
c = float("3") # c will be 3.0
print(a)
print(b)
print(c)


# String:
p = str("s1")
q = str(2)
r = str(3.0)
print(p)
print(q)
print(r)


