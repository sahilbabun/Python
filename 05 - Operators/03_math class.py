#  Constants and functions from math class

import math

x = 9

print(math.pi)
print(math.e)

result = math.sqrt(x)
print(result)

x = 9.1
result = math.ceil(x)  # ceil is always around a floating point number up.
print(result)    # result is: 10

x = 9.9
result = math.floor(x)  # floor is always around a floating point number down.
print(result)           # result is: 9

