# Escape Sequence Characters:

a = "Sahil is a good boy\nbut not a bad boy."
print(a)

# '\"' : The escape character allows you to use double quotes when you normally would not be allowed.
txt = "Sahil is a good boy but not a \"bad\" boy."
print(txt)


# \r : Carriage Return.
txt = "Hello\rWorld!"
print(txt) 


# Other escape characters:

#    \' : Single quote character.
#    \\ : Backslash character.
#    \t : Tab character.
#    \b : Backspace character.
#    \f : Fenced character.
#    \ooo : Octal character.
#    \xhh : Hexadecimal character.

