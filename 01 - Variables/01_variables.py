# Variable = A container for a value (string, integer, float, boolean) for storing data values.
#            A variable behaves as if it was the value it contains.

# Variables do not need to be declared with any particular type, 
# ...and can even change type after they have been set.

# Integers
a = 1           # a is of type int
b = 2           # b is of type
print(a+b)

age = 24
print(age)
print(f"Your age is {age} years")

quantity = 3
print(f"You are buying {quantity} items")

num_of_students = 100
print(f"Your class has {num_of_students} students")


# Float
price = 10.99   # price is of type float
print(f"Your price is ${price}")

cgpa = 8.49
print(f"Your cgpa is {cgpa}")


# String
name = "Python"  # Python is now of type string.
print(name)
print(F"Hello {name}")

first_name = "Bro"
print(f"hello {first_name}")  # f-string - formatted string literals, either you can use 'f' or 'F' respectively.

food = "Biryani"
print(f"I love {food}")

email = "Bro123@fake.com"
print(f"your email is {email}")


# Boolean
is_student = True
print(f"Are you a student?: {is_student}")

if is_student:
    print("You are a student.")
else:
    print("You are NOT a student")

# None
e = None   # None:- ye mujhe tab denote karna hai, kisi particular variable mei kuch bhi nahi hai, tab mei None istemal karunga.

